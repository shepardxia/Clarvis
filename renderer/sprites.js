/**
 * Clarvis Sprite - Single static ASCII art
 */

// Color palette (character -> RGBA)
const PALETTE = {
  '#': '100, 100, 110, 255',   // Body
  '+': '140, 140, 150, 255',   // Light accent
  '*': '120, 120, 130, 255',   // Mid accent
  ':': '180, 180, 190, 255',   // Eye white
  '=': '160, 160, 170, 255',   // Highlight
  '%': '60, 60, 70, 255',      // Pupil
  '-': '80, 80, 90, 255',      // Dark accent
  ' ': '0, 0, 0, 0',           // Transparent
};

// Single ASCII frame
const ASCII = `
               ##########
              ############
         ######################
       ###########################
      ############################
    ##+:::::*#########+:::::*#######
    ++=:::::++*#####++=:::::++*#####
    ::::::::::-#####::::::::::-#####
    %%%%%=::::-#####%%%%%=::::-#####
 ###%%%%%=::::-#####%%%%%=::::-########
 ###%%%%%=::::-#####%%%%%=::::-########
 #####+:::::*#########+:::::*##########
 #####*=====##########*=====###########
 ######################################
 ######################################
 ######################################
 ######################################
 ######################################
 ######################################
 ######  #########    #########  ######
 ###        ######    ######        ###
 ###        #####      #####        ###
`;

// Convert ASCII to pixel data
function asciiToPixels(ascii, palette) {
  return ascii.trim().split('\n').map(line =>
    [...line].map(char => palette[char] || palette[' '])
  );
}

const SPRITE = asciiToPixels(ASCII, PALETTE);

// Export
window.SPRITE = SPRITE;
window.PALETTE = PALETTE;
